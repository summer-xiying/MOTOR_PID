#ifndef MOTOR_H
#define MOTOR_H

#include "ti_msp_dl_config.h"

/*
 *  ========================================
 *  双电机引脚配置表
 *  ========================================
 *
 *  电机1 (TB6612 A通道) - 已配置:
 *  ┌────────────┬────────────┬──────────────────────────────────────┐
 *  │  G3507     │  TB6612    │  功能说明                            │
 *  ├────────────┼────────────┼──────────────────────────────────────┤
 *  │  PB24      │  STBY      │  使能控制 (两个电机共用)             │
 *  │  PA8       │  AIN1      │  方向控制1 (GPIO输出)                │
 *  │  PA9       │  AIN2      │  方向控制2 (GPIO输出)                │
 *  │  PA12      │  PWMA      │  PWM调速 (TIMG0 Channel0 CCP0)      │
 *  └────────────┴────────────┴──────────────────────────────────────┘
 *
 *  电机1 编码器 - 已配置:
 *  ┌────────────┬────────────┬──────────────────────────────────────┐
 *  │  G3507     │  编码器    │  功能说明                            │
 *  ├────────────┼────────────┼──────────────────────────────────────┤
 *  │  PA17      │  A         │  编码器A相 (GPIO中断, 上升沿)        │
 *  │  PA22      │  B         │  编码器B相 (悬空或接GND)             │
 *  │  3V3       │  VCC       │  供电                                │
 *  │  GND       │  GND       │  地线                                │
 *  └────────────┴────────────┴──────────────────────────────────────┘
 *
 *  电机2 (TB6612 B通道) - 需要在SysConfig配置:
 *  ┌────────────┬────────────┬──────────────────────────────────────┐
 *  │  G3507     │  TB6612    │  功能说明                            │
 *  ├────────────┼────────────┼──────────────────────────────────────┤
 *  │  PB24      │  STBY      │  使能控制 (与电机1共用, 已配置)      │
 *  │  PB0       │  BIN1      │  方向控制1 (GPIO输出)                │
 *  │  PB1       │  BIN2      │  方向控制2 (GPIO输出)                │
 *  │  PA21      │  PWMB      │  PWM调速 (TIMG1 Channel0 CCP0)      │
 *  └────────────┴────────────┴──────────────────────────────────────┘
 *
 *  电机2 编码器 - 需要在SysConfig配置:
 *  ┌────────────┬────────────┬──────────────────────────────────────┐
 *  │  G3507     │  编码器    │  功能说明                            │
 *  ├────────────┼────────────┼──────────────────────────────────────┤
 *  │  PB16      │  A         │  编码器A相 (GPIO中断, 上升沿)        │
 *  │  PB17      │  B         │  编码器B相 (悬空或接GND)             │
 *  │  3V3       │  VCC       │  供电                                │
 *  │  GND       │  GND       │  地线                                │
 *  └────────────┴────────────┴──────────────────────────────────────┘
 *
 *  电源连接:
 *  ┌────────────┬────────────┬──────────────────────────────────────┐
 *  │  TB6612    │  电源模块  │  说明                                │
 *  ├────────────┼────────────┼──────────────────────────────────────┤
 *  │  VM        │  7.4V      │  电机电源                            │
 *  │  VCC       │  3.3V      │  逻辑电源 (可从G3507取)              │
 *  │  GND       │  GND       │  共地 (所有GND必须连在一起)          │
 *  └────────────┴────────────┴──────────────────────────────────────┘
 *
 *  注意: 所有的GND都需要连接在一起
 */

// 电机ID定义
#define MOTOR_1    1
#define MOTOR_2    2

// 编码器参数
#define PI 3.14
#define MOTOR_BIANMAQI 260    // 编码器线数
#define MOTOR_WHEEL_D  47     // 轮胎直径 mm

/*
 *  ========================================
 *  电机控制函数
 *  ========================================
 */

// 电机初始化
void motor_init(uint8_t motor_id);

// 设置PWM占空比 (0~4000)
void motor_set_duty(uint8_t motor_id, uint32_t duty);

// 设置电机方向 (0:停止, 1:正转, 2:反转)
void motor_set_direction(uint8_t motor_id, uint8_t direction);

#endif // MOTOR_H
